package Chapter_2;

public class Exampl_3_CharArithDemo {
    public static void main(String[] args) {
        char ch;
        ch = 'X';
        System.out.println("ch содержит " + ch);

        ch++;
        System.out.println("ch содержит " + ch);

        ch = 90;
        System.out.println("ch содержит " + ch);
    }
}
